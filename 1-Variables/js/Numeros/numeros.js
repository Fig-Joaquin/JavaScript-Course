
let resultado;

//Redondeo de numero
resultado = Math.round(2.6); // 3
resultado = Math.round(2.3); // 2
console.log(resultado);

//Redondear hacia arriba
resultado = Math.ceil(2.2) // 3
console.log(resultado)

//Redondear hacia abajo
resultado = Math.floor(2.6) // 2
console.log(resultado)

//Raiz cuadrada
resultado = Math.sqrt(9)
console.log(resultado)

//Valor absoluto 
resultado = Math.abs(-10)
console.log(resultado)

//Random
resultado = Math.ceil(Math.random()*20) // numero random del 0 al 20
console.log(resultado)

